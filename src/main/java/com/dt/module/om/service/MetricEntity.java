public class MetricEntity {

	private String id = "";
	private String name = "";
	private String ds = "";
	private String showtype = "";
	private String chartopt = "";
	private String cols = "";
	private String mark = "";

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the ds
	 */
	public String getDs() {
		return ds;
	}

	/**
	 * @param ds
	 *            the ds to set
	 */
	public void setDs(String ds) {
		this.ds = ds;
	}

	/**
	 * @return the showtype
	 */
	public String getShowtype() {
		return showtype;
	}

	/**
	 * @param showtype
	 *            the showtype to set
	 */
	public void setShowtype(String showtype) {
		this.showtype = showtype;
	}

	/**
	 * @return the chartopt
	 */
	public String getChartopt() {
		return chartopt;
	}

	/**
	 * @param chartopt
	 *            the chartopt to set
	 */
	public void setChartopt(String chartopt) {
		this.chartopt = chartopt;
	}

	/**
	 * @return the cols
	 */
	public String getCols() {
		return cols;
	}

	/**
	 * @param cols
	 *            the cols to set
	 */
	public void setCols(String cols) {
		this.cols = cols;
	}

	/**
	 * @return the mark
	 */
	public String getMark() {
		return mark;
	}

	/**
	 * @param mark
	 *            the mark to set
	 */
	public void setMark(String mark) {
		this.mark = mark;
	}
}
